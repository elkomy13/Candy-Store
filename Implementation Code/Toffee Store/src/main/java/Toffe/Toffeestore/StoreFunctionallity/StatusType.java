package Toffe.Toffeestore.StoreFunctionallity;

/**

 An enumeration class representing the status of a product.
 The two possible values are INstock and OUTofstock.
 */
public enum StatusType  {
    /**
    Represents that the product is in stock.
    */
    INstock,
    /**
     Represents that the product is out of stock.
     */
    OUTofstock;

}
